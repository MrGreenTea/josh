#[allow(unused_imports)]
use std::io::{self, Write};

fn main() {
    print!("$ ");
    io::stdout().flush().unwrap();

    // Wait for user input
    let stdin = io::stdin();
    let mut input = String::new();
    let command = stdin.read_line(&mut input).unwrap();
    println!("{}: Command not found", command);
}
